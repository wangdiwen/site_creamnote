      <h2><b>关于醍醐</b></h2>
      <div class="text_line">
        <p style="font-size:16px"><b>醍醐是：</b></p>
        <p style="font-size:16px"><b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一个专注于高校大学生的专业课堂学习笔记、期末考试习题、还有考研复习资料的分享与展示平台</b></p>
        <p style="font-size:16px"><b>醍醐的愿景是：</b></p>
        <p style="font-size:16px"><b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;服务高校师生、促进知识共享、开创未来中国高等教育的Web3.0时代。</b></p>
        <br />
        <p style="font-size:13px">我们开发和运维团队的成员都是毕业1-2年的青年大学生，有的才刚刚开始实习参加工作。</p>
        <p style="font-size:13px">我们切身体会在校大学生们在各种考试、会考来临的时候，到处在班级的各个兄弟宿舍间“走访”、“串门”的紧张又有点兴奋的心情，当每学期的期末考试在明天开考的前一天晚上，大家伙夜不能寐啊，每见到一个同学，开口就是“嗨，明天的通信原理考试
          准备好小抄了吗？”、“嗨，有没有去年的考试试卷呀？”“女生那边有没有整理好的学习笔记呀？”（还有很多众里寻考试资料千百度的见面问候语）
        </p>
        </br>
        <p style="font-size:14px"><b>因为经历过，所以我们创办了醍醐笔记网，希望醍醐是中国第一个面向高校师生的学习笔记、考试资料分享社区平台</b></p>
        <p style="font-size:13px">不论你是认真学习的“学霸”也好、还是上课玩手机、考试带小抄的屌丝，亦或者你是一位想着班上同学期末考试不挂科的高校老师，您都可以从醍醐收获想象不到的意外惊喜</p>
        </br>
        <p style="font-size:14px"><b>在醍醐，你可以：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、在醍醐上面珍藏一份自己大学时代的学习资料；</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、分享你的学习笔记和考试、考研资料给学弟、学妹们，让他们在考试的前一天晚上可以安心的睡个好觉；</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、从醍醐获取分享学习笔记、资料的收益，醍醐保证只从付费的笔记、资料的当次下载中抽取20%的费用，作为平台运维开支的一个小小补充。</p>
        </br>
        <p style="font-size:14px"><b>如果您：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、是一个“学霸”，那么你可以上传分享和展示自己的学习笔记，或者是考试资料也成；</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、是一位和我曾经一样的“每逢考试就带小抄”的屌丝，那么你可以在醍醐上面随时搜索你们想要的各种学习笔记、历年的
          专业考试试卷、还有“学霸”精心整理好的期末复习大纲和题目的答案哦；</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、是一位考研的同学，那么醍醐希望你经常逛逛，说不定你碰巧了，搜索到了你报考那个NB大学研究生院的内部的考试大纲，那么醍醐
          的某位用户上传的这份考研资料，就助力你考上了研究生，到那时，很难想象你开心的样子咯；</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4、是一位高校的老师，那么没关系，醍醐希望你也是其中的一位热心用户，在教学过程中，作为老师，你真心希望自己的学生学习学的好，
          考试考的好，但有的时候，总有那一部分同学就是“不争气”，你有担心他们在期末考试中“各种挂”，不要紧，你作为老师，可以匿名在醍醐上注册，把自己讲课的PPT、
          期末专业考试的复习资料分享出来，那么班上的同学在醍醐上面就能搜索到您提供的一手考试复习资料了，这样一来，同学有了复习的资料，学习的劲头自然也提高了不是？</p>
        </br>
        <p style="font-size:14px"><b>在醍醐，我们开创性的把笔记资料分成2种授权使用方式，免费和付费。我们的付费下载资料不是采用虚拟积分的，而是真实的货币哦（￥、RMB）。
        </b></p>
        </br>
        <p style="font-size:14px"><b>有的同学要说了：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;你们为什么要采取收费下载呀？为什么是实际货币付费呀？像百度文库、道客巴巴这样的资料平台都是采用积分的方式，顶多赞一下别人分享的东西，然后随便写个评论，积分自然就来了，这样就不要让大家掏腰包了？</p>
        </br>
        <p style="font-size:14px"><b>我们的回答是：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、老套的积分方式，现在已经是“古董”级的东西了，它只是表面上促进了大家对平台的关注度，但并不能从根本上保证平台上面的东西都是高质量的。</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、我们醍醐上面的所有笔记、资料都是经过人工审核的方式，才能开放给用户使用的，也就是说，同学搜索到的资料都是货真价实的，是优秀的学习笔记、
          期末考试习题资料、考研复习资料。</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、为了体谅和照顾到“学霸”整理一份优秀的、可用的、对大家学习有积极作用的学习笔记实在是一件不容易的事情，人家也确实付出了辛勤努力的汗水，
          所以我们开放了学习笔记资料的付费下载使用这个渠道。其目的是在方便同学的同时，也给那些把自己的干货分享出来的同学得到一份意外的收益惊喜，但是免费也好、付费也好，我们把权利交给分享干货的用户。</p>
        <br />
        <p style="font-size:14px"><b>“醍醐”的前世今生：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相信大家都会觉得比较奇怪，这个笔记资料的分享网站，为什么叫“醍醐”这个奇葩的名字呀？？？</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个问题呢，与醍醐的创始人（Steven Wang）有点不搭嘎的关系，Steven 是半个佛教信徒，他是一个“理性、感性参半”的人，当他开始准备创办一个教育类的新型网站的时候，
        第一个考虑的就是平台要成为所有同类别中的“精华”，不仅网站要新颖、有特色，让人“眼前一亮”的赶脚，平台上面的内容更要高质量。</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;偶然的机缘，Steven由“精华”和“让人眼前一亮”的主旨，联想到佛教的用语“醍醐”，更有传统成语“醍醐灌顶”做媒，所以在几位联合创始人的一致认同下，
        Steven把网站定名为“醍醐”，而且为了保障“醍醐”名称的所有权，同时将自己的公司名称定为“醍醐网络科技”，希望自己的公司，可以与“醍醐”网站一起成长、壮大，成为一家“精华荟萃”的企业；
        而平台以分享学习笔记、资料起家，故而将“笔记”与“醍醐”结合，这便是“醍醐笔记”的前世今生啦 ^_^</p>
        <br />
        <p style="font-size:14px"><b>“醍醐”佛教教义：</b></p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;醍醐原本指酥酪上凝聚的精油、奶酪，现寓意本质、精华、精髓之意。</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《涅槃经》卷十四云：“譬如从牛出乳，从乳出酪，从酪出生苏，从生苏出熟苏，从熟苏出醍醐，醍醐最上，若有服者，众病皆除，所有诸药，皆入其中。”
        以之比喻佛出于《十二部经》，从《十二部经》出《修多罗》，从《修多罗》出《方等经》，从《方等经》出《般若波罗蜜》，从《般若波罗蜜》出《大涅槃》，犹如醍醐。</p>
        <p style="font-size:13px">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;自唐以后，“醍醐”一词不仅用来形象地说明所谓佛教信徒成佛的渐进修炼过程；还可以比喻佛教各派教义，在相互辩驳、诘难中，不断提升的经历。</p>
